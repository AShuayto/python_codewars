def add(a,b):
    return a + b

def multiply(a,b):
    return a * b

def divide(a,b):
    return int(a / b)

def mod(a,b):
    return a % b

def exponent(a,b):
    return a ** b

def subtraction(a,b):
    return a - b



print(add(1,3))
print(multiply(5,1))
print(divide(6,2))
print(mod(6,2))
print(exponent(2,2))